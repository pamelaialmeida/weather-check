package br.com.weathercheck.models;

/**
 * The class represents the wind object. 
 * 
 * @author Pamela Inacio Almeida
 */
public class Wind {
	
	private float speed;
	private int deg;
	
	public float getSpeed() {
		return speed;
	}
	
	public void setSpeed(float speed) {
		this.speed = speed;
	}
	
	public int getDeg() {
		return deg;
	}
	
	public void setDeg(int deg) {
		this.deg = deg;
	}	

}
